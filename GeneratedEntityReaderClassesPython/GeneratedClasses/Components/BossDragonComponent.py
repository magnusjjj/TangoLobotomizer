    target_kill_ragdoll_force:NoitaFloat = field(default_factory=NoitaFloat)
    hunt_box_radius:NoitaFloat = field(default_factory=NoitaFloat)
    random_target_box_radius:NoitaFloat = field(default_factory=NoitaFloat)
