        self.bite_damage.Read(s)
        self.target_kill_radius.Read(s)
        self.target_kill_ragdoll_force.Read(s)
        self.hunt_box_radius.Read(s)
        self.random_target_box_radius.Read(s)
        self.new_hunt_target_check_every.Read(s)
        self.new_random_target_check_every.Read(s)
        self.jump_cam_shake.Read(s)
        self.jump_cam_shake_distance.Read(s)
        self.eat_anim_wait_mult.Read(s)
        self.projectile_1.Read(s)
        self.projectile_1_count.Read(s)
        self.projectile_2.Read(s)
        self.projectile_2_count.Read(s)
