    projectile_1_count:NoitaInt = field(default_factory=NoitaInt)
    projectile_2:NoitaString = field(default_factory=NoitaString)
    projectile_2_count:NoitaInt = field(default_factory=NoitaInt)
    ragdoll_filename:NoitaString = field(default_factory=NoitaString)
    
