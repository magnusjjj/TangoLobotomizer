    fly_model_player:NoitaBool = field(default_factory=NoitaBool)
    fly_smooth_y:NoitaBool = field(default_factory=NoitaBool)
    accel_x:NoitaFloat = field(default_factory=NoitaFloat)
    accel_x_air:NoitaFloat = field(default_factory=NoitaFloat)
    pixel_gravity:NoitaFloat = field(default_factory=NoitaFloat)
    swim_idle_buoyancy_coeff:NoitaFloat = field(default_factory=NoitaFloat)
    swim_down_buoyancy_coeff:NoitaFloat = field(default_factory=NoitaFloat)
    swim_up_buoyancy_coeff:NoitaFloat = field(default_factory=NoitaFloat)
    swim_drag:NoitaFloat = field(default_factory=NoitaFloat)
    swim_extra_horizontal_drag:NoitaFloat = field(default_factory=NoitaFloat)
    mouse_look:NoitaBool = field(default_factory=NoitaBool)
    mouse_look_buffer:NoitaFloat = field(default_factory=NoitaFloat)
    keyboard_look:NoitaBool = field(default_factory=NoitaBool)
    turning_buffer:NoitaFloat = field(default_factory=NoitaFloat)
    animation_to_play:NoitaString = field(default_factory=NoitaString)
    animation_to_play_next:NoitaString = field(default_factory=NoitaString)
    run_animation_velocity_switching_threshold:NoitaFloat = field(default_factory=NoitaFloat)
    run_animation_velocity_switching_enabled:NoitaBool = field(default_factory=NoitaBool)
    turn_animation_frames_between:NoitaInt = field(default_factory=NoitaInt)
    precision_jumping_max_duration_frames:NoitaInt = field(default_factory=NoitaInt)
    audio_liquid_splash_intensity:NoitaFloat = field(default_factory=NoitaFloat)
    
