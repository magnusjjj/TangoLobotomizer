        self.accel_x_air.Read(s)
        self.pixel_gravity.Read(s)
        self.swim_idle_buoyancy_coeff.Read(s)
        self.swim_down_buoyancy_coeff.Read(s)
        self.swim_up_buoyancy_coeff.Read(s)
        self.swim_drag.Read(s)
        self.swim_extra_horizontal_drag.Read(s)
        self.mouse_look.Read(s)
        self.mouse_look_buffer.Read(s)
        self.keyboard_look.Read(s)
        self.turning_buffer.Read(s)
        self.animation_to_play.Read(s)
        self.animation_to_play_next.Read(s)
        self.run_animation_velocity_switching_threshold.Read(s)
