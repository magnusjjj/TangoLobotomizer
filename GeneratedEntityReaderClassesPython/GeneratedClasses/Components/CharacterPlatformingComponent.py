    jump_velocity_x:NoitaFloat = field(default_factory=NoitaFloat)
    jump_velocity_y:NoitaFloat = field(default_factory=NoitaFloat)
    jump_keydown_buffer:NoitaInt = field(default_factory=NoitaInt)
    fly_speed_max_up:NoitaLensValue = field(default_factory=lambda: NoitaLensValue(NoitaFloat))
    fly_speed_max_down:NoitaLensValue = field(default_factory=lambda: NoitaLensValue(NoitaFloat))
    fly_speed_mult:NoitaFloat = field(default_factory=NoitaFloat)
