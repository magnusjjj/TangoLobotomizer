        self.ui_sprite.Read(s)
        self.ui_description.Read(s)
        self.preferred_inventory.Read(s)
        self.enable_orb_hacks.Read(s)
        self.is_all_spells_book.Read(s)
        self.always_use_item_name_in_ui.Read(s)
