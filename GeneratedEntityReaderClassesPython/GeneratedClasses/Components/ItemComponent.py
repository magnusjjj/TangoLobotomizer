    max_child_items:NoitaInt = field(default_factory=NoitaInt)
    ui_sprite:NoitaString = field(default_factory=NoitaString)
    ui_description:NoitaString = field(default_factory=NoitaString)
    preferred_inventory:NoitaUInt = field(default_factory=NoitaUInt)
    enable_orb_hacks:NoitaBool = field(default_factory=NoitaBool)
    is_all_spells_book:NoitaBool = field(default_factory=NoitaBool)
    always_use_item_name_in_ui:NoitaBool = field(default_factory=NoitaBool)
    custom_pickup_string:NoitaString = field(default_factory=NoitaString)
    ui_display_description_on_pick_up_hint:NoitaBool = field(default_factory=NoitaBool)
