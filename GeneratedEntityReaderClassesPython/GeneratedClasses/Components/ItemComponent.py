    is_hittable_always:NoitaBool = field(default_factory=NoitaBool)
    item_pickup_radius:NoitaFloat = field(default_factory=NoitaFloat)
    camera_max_distance:NoitaFloat = field(default_factory=NoitaFloat)
    camera_smooth_speed_multiplier:NoitaFloat = field(default_factory=NoitaFloat)
    has_been_picked_by_player:NoitaBool = field(default_factory=NoitaBool)
    mFramePickedUp:NoitaInt = field(default_factory=NoitaInt)
    
