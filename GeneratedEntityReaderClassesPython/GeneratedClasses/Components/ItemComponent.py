    collect_nondefault_actions:NoitaBool = field(default_factory=NoitaBool)
    remove_on_death:NoitaBool = field(default_factory=NoitaBool)
    remove_on_death_if_empty:NoitaBool = field(default_factory=NoitaBool)
    remove_default_child_actions_on_death:NoitaBool = field(default_factory=NoitaBool)
    play_hover_animation:NoitaBool = field(default_factory=NoitaBool)
    play_spinning_animation:NoitaBool = field(default_factory=NoitaBool)
    is_equipable_forced:NoitaBool = field(default_factory=NoitaBool)
    play_pick_sound:NoitaBool = field(default_factory=NoitaBool)
    drinkable:NoitaBool = field(default_factory=NoitaBool)
