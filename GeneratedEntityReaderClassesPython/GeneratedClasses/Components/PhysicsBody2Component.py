        self.force_add_update_areas.Read(s)
        self.update_entity_transform.Read(s)
        self.kill_entity_if_body_destroyed.Read(s)
        self.kill_entity_after_initialized.Read(s)
        self.manual_init.Read(s)
        self.destroy_body_if_entity_destroyed.Read(s)
        self.root_offset_x.Read(s)
        self.root_offset_y.Read(s)
        self.init_offset_x.Read(s)
