    init_offset_y:NoitaFloat = field(default_factory=NoitaFloat)
    mActiveState:NoitaBool = field(default_factory=NoitaBool)
    mLocalPosition:NoitaCVector2 = field(default_factory=lambda: NoitaCVector2(NoitaFloat))
