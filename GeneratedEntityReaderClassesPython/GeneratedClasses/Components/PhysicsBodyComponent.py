    angular_damping:NoitaFloat = field(default_factory=NoitaFloat)
    allow_sleep:NoitaBool = field(default_factory=NoitaBool)
    fixed_rotation:NoitaBool = field(default_factory=NoitaBool)
    buoyancy:NoitaFloat = field(default_factory=NoitaFloat)
    gravity_scale_if_has_no_image_shapes:NoitaFloat = field(default_factory=NoitaFloat)
    is_bullet:NoitaBool = field(default_factory=NoitaBool)
    is_static:NoitaBool = field(default_factory=NoitaBool)
    is_kinematic:NoitaBool = field(default_factory=NoitaBool)
    is_character:NoitaBool = field(default_factory=NoitaBool)
    go_through_sand:NoitaBool = field(default_factory=NoitaBool)
    gridworld_box2d:NoitaBool = field(default_factory=NoitaBool)
    auto_clean:NoitaBool = field(default_factory=NoitaBool)
    on_death_leave_physics_body:NoitaBool = field(default_factory=NoitaBool)
    on_death_really_leave_body:NoitaBool = field(default_factory=NoitaBool)
    update_entity_transform:NoitaBool = field(default_factory=NoitaBool)
    force_add_update_areas:NoitaBool = field(default_factory=NoitaBool)
    kills_entity:NoitaBool = field(default_factory=NoitaBool)
    projectiles_rotate_toward_velocity:NoitaBool = field(default_factory=NoitaBool)
    initial_velocity:NoitaCVector2 = field(default_factory=lambda: NoitaCVector2(NoitaFloat))
    randomize_init_velocity:NoitaBool = field(default_factory=NoitaBool)
    mActiveState:NoitaBool = field(default_factory=NoitaBool)
    
