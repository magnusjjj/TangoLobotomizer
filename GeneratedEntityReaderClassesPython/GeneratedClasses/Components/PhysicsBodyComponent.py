        self.Enabled.Write(s)
        self.Tags.Write(s)
        self.is_external.Write(s)
        self.hax_fix_going_through_ground.Write(s)
        self.hax_fix_going_through_sand.Write(s)
        self.hax_wait_till_pixel_scenes_loaded.Write(s)
        self.uid.Write(s)
        self.is_enabled.Write(s)
        self.linear_damping.Write(s)
        self.angular_damping.Write(s)
        self.allow_sleep.Write(s)
        self.fixed_rotation.Write(s)
        self.buoyancy.Write(s)
        self.gravity_scale_if_has_no_image_shapes.Write(s)
        self.is_bullet.Write(s)
        self.is_static.Write(s)
        self.is_kinematic.Write(s)
        self.is_character.Write(s)
